"Em, C# 10, const strings podem ser inicializadas usando interpolação de string, 
se todas as strings interpoladas forem também constantes. A interpolação de strings pode 
criar strings constantes mais legíveis conforme você as produza e faça uso em sua aplicação. 
Os valores passados para interpolação de string constante não podem conter números contantes, pois 
os valores dessas contantes numéricas são convertidas para string em tempo de execução. A cultura
em uso da aplicação pode afetar a representação da string."
